var d = new Date();
console.log(d);
console.log(d.getFullYear());//2016, 年份
console.log(d.getMonth()); // 10, 月份，注意月份范围是0~11，10表示11月
console.log(d.getDay());//4 表示星期四
console.log(d.getHours());//10 24小时制
console.log(d.getMinutes()); //28 分钟
console.log(d.getSeconds()); //2 秒
console.log(d.getMilliseconds());//836 毫秒
console.log(d.getTime());//以number形式显示时间戳